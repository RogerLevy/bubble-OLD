\ potential seed of the successor to Apprentice

\ a GUI/IDE for creating workspaces and tools for working on games

\ elements
\   sticker - an image
\   text - sometimes-editable multiline string
\   peeker - sometimes-editable numeric value or array of values
\   board - customizable rectangular area, multipurpose

\ prefabs
\   button - pressable, toggleable, executable text on a board
\   toolbox - openable, labeled containers
\   workspace - a special container


\ functions
\   viewing sections of memory realtime
\   creating little "workshops" for creating various gameware
\   automatically creating buttons out of recently defined words
\   rapidly creating interfaces out of words as buttons
\   

\ side ideas
\   optional redefined `!` that enables memory watching
